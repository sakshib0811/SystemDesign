class Seat {
    String seatId;
    String trainNo;
    String coachNo;
    String classType;
    String birthType;

    Seat(String seatId, String trainNo, String coachNo, String classType, String birthNo) {
        this.trainNo = trainNo;
        this.seatId = seatId;
        this.coachNo = coachNo;
        this.classType = classType;
        this.birthType = birthNo;

    }
}
